// iter 135 — `gemini plugin-init`. Backfills .claude-plugin/plugin.json
// for legacy harnesses scaffolded before iter-131. The doctor WARN
// (iter 134) recommends this as the remediation path.
//
// Why a standalone subcommand instead of `gemini upgrade`?
//   - `gemini upgrade` is a copier-style template diff that needs the
//     full new-template render to compute fingerprints. Heavy for one
//     missing file.
//   - This is a focused, deterministic emitter — read .gemini/manifest.json
//     for the gemini name + description, write .claude-plugin/plugin.json
//     with the canonical iter-131/132 shape.
//   - Idempotent + dry-run-by-default: re-runnable without surprise.

import { existsSync } from 'node:fs';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';

export interface PluginInitResult {
  code: number;
  lines: string[];
}

interface ManifestMeta {
  name?: string;
  description?: string;
  domain?: string;
  templateId?: string;
}

interface HarnessManifest {
  meta?: ManifestMeta;
  name?: string;
  description?: string;
  template?: string;
}

function manifestSummary(m: HarnessManifest): {
  name: string;
  description: string;
  templateId: string | undefined;
  domain: string | undefined;
} {
  // Tolerate both top-level fields (older manifests) and meta.* (newer).
  return {
    name: m.meta?.name ?? m.name ?? 'unnamed-gemini',
    description: m.meta?.description ?? m.description ?? 'AI agent gemini',
    templateId: m.meta?.templateId ?? m.template,
    domain: m.meta?.domain,
  };
}

function uniq<T>(arr: T[]): T[] { return [...new Set(arr)]; }

function buildPluginJson(s: ReturnType<typeof manifestSummary>): string {
  const cats = uniq(['agent-gemini', 'metaharness-scaffold', ...(s.templateId?.startsWith('vertical:') ? ['vertical'] : []), ...(s.domain ? [s.domain] : [])]);
  const tags = uniq(['metaharness', 'agent-gemini', ...(s.templateId ? [s.templateId] : []), ...(s.domain ? [s.domain] : [])]);
  return JSON.stringify({
    name: s.name,
    version: '0.1.0',
    description: s.description,
    author: {
      displayName: 'Generated by metaharness',
      url: 'https://www.npmjs.com/package/metaharness',
    },
    license: 'MIT',
    categories: cats,
    tags,
    homepage: 'https://github.com/ruvnet/agent-gemini-generator',
  }, null, 2) + '\n';
}

export async function pluginInitCmd(args: string[]): Promise<PluginInitResult> {
  let dryRun = false;
  let force = false;
  let dir = process.cwd();
  for (const a of args) {
    if (a === '--dry-run') dryRun = true;
    else if (a === '--force') force = true;
    else if (!a.startsWith('--')) dir = a;
  }
  dir = resolve(dir);
  const lines: string[] = [];
  lines.push(`gemini plugin-init — ${dir}${dryRun ? ' (dry-run)' : ''}`);

  const manifestPath = join(dir, '.gemini', 'manifest.json');
  if (!existsSync(manifestPath)) {
    lines.push(`  FAIL no .gemini/manifest.json — not a metaharness scaffold`);
    return { code: 1, lines };
  }
  let manifest: HarnessManifest;
  try {
    manifest = JSON.parse(await readFile(manifestPath, 'utf-8'));
  } catch (err) {
    lines.push(`  FAIL .gemini/manifest.json is not valid JSON: ${err instanceof Error ? err.message : String(err)}`);
    return { code: 1, lines };
  }
  const summary = manifestSummary(manifest);
  const pluginPath = join(dir, '.claude-plugin', 'plugin.json');

  if (existsSync(pluginPath) && !force) {
    lines.push(`  SKIP .claude-plugin/plugin.json already exists (use --force to overwrite)`);
    return { code: 0, lines };
  }

  const body = buildPluginJson(summary);
  if (dryRun) {
    lines.push(`  would-write ${pluginPath}`);
    lines.push(`  preview:`);
    for (const line of body.trimEnd().split('\n')) lines.push(`    ${line}`);
    return { code: 0, lines };
  }
  await mkdir(dirname(pluginPath), { recursive: true });
  await writeFile(pluginPath, body, 'utf-8');
  lines.push(`  ${existsSync(pluginPath) && force ? 'OVERWROTE' : 'WROTE'} ${pluginPath}`);
  lines.push(`  name=${summary.name}`);
  lines.push(`  next: claude -p --plugin-dir "${dir}" "Reply with: ${summary.name}_OK"`);
  return { code: 0, lines };
}
